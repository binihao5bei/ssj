l=['Python','is','my','favourite','languange']
str_1=''.join(l)   #用join方法，合并列表的元素
print(str_1)
#输出结果：Pythonismyfavouritelanguange
str_2=' '.join(l)   #用join方法，合并列表的元素
print(str_2)
#输出结果：Python is my favourite languange
